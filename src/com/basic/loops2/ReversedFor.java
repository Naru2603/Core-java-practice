package com.basic.loops2;

public class ReversedFor {

	public static void main(String[] args) {
		
		for (int i=6;i>=0;i--) {
			System.out.println(i);
		}
		
		System.out.println("==================");
		
		int [] number = {12,31,1,3,234,5,6,23};
		
		for(int i=0;i<number.length;i++) {
			System.out.println(number[i]);
		}
	}

}
